def clean_zipcode(zipcode)
  zipcode.to_s.rjust(5,"0")[0..4]
end

def check_phone(phone)
  checked_phone = phone.gsub(/\D/, "") # Elimina todos los caracteres no num√©ricos

  case checked_phone.length
  when 0..9
    return "bad number"
  when 10
    return checked_phone
  when 11
    if checked_phone[0] == '1'
      checked_phone = checked_phone[1..-1] 
      return checked_phone
    else
      return "bad number"
    end
  else
    return "bad number"
  end
end

